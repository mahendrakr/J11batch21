package java11;

public class AssignValueToDataType {
	byte b = 10;
	short s = 20;
	int i = 30;
	long l = 400;
	float f = 40.40f;
	double d = 50.50;
	char c = 'a';
	String str = "My name is Aadi";
public static void main(String[] args) {
	AssignValueToDataType av = new AssignValueToDataType();
	System.out.println(av.b);
	System.out.println(av.s);
	System.out.println(av.i);
	System.out.println(av.l);
	System.out.println(av.f);
	System.out.println(av.d);
	System.out.println(av.c);
	System.out.println(av.str);
}
}
